    //obtiene el evento click del mouse en la tabla y le asignamos los valores a los campos.
    private void tblCarteleraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCarteleraMouseClicked
        try {
            int row = tblCartelera.getSelectedRow();
            int Act = 0;
            int AplPro = 0;
            Id = (int) tblCartelera.getValueAt(row, 0);
            PreparedStatement ps = ObjCon.abrir().prepareStatement("SELECT IdCartelera ,cartelera.IdSala ,cartelera.IdPelicula as IdPelicula,if(AplicaProm>0,'SI','NO') as AplicaProm,if(Activa >0,'SI','NO') as Activa,NroSala ,Capacidad ,HoraApertura,Tecnologia ,Nombre , Duracion , Categoria,(Capacidad - (CantOcupadaSala + AsientosPref)) as CantOcupadaSala,AsientosPref \n"
                    + "FROM `cartelera` \n"
                    + "LEFT JOIN salascn ON salascn.IdSala = cartelera.IdSala\n"
                    + "LEFT JOIN peliculas ON peliculas.IdPelicula = cartelera.IdPelicula\n"
                    + "WHERE IdCartelera=" + Id);
            ResultSet rs = ObjCon.ejecutarSP(ps); //Ejecutar procedimiento almacenado o vista
            ObjCon.cerrar();
            //LLenamos la variable con la consulta.
            while (rs.next()) {
                if (tblCartelera.getValueAt(row, 6).equals("SI")) {
                    Act = 1;
                }
                if (tblCartelera.getValueAt(row, 5).equals("SI")) {
                    AplPro = 1;
                }
                cbxNroSala.setSelectedItem("#" + tblCartelera.getValueAt(row, 1));
                cbxIdPelicula.setSelectedItem(rs.getInt(3)+"-"+tblCartelera.getValueAt(row, 3));
                if (Act >= 1) {
                    ChkActivaSi.setSelected(true);
                } else {
                    ChkActivaSi.setSelected(false);
                }
                if (AplPro >= 1) {
                    ChkAplicapSi.setSelected(true);
                } else {
                    ChkAplicapSi.setSelected(false);
                }
            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_tblCarteleraMouseClicked

