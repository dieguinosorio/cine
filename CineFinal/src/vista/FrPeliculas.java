    private void bntEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntEliminarActionPerformed
        // TODO add your handling code here:                                
        //Obtener la fila seleccionada
        int fila = TblPeliculas.getSelectedRow();

        //Si es mayor o igual a cero, entonces, existe fila.
        if (fila >= 0) {
            //Instanciamos el objeto Producto
            Peliculas objPel = new Peliculas();
            //Asignamos valor al id
            objPel.setIdPelicula(Integer.parseInt(TblPeliculas.getValueAt(fila, 0).toString()));
            if(objPel.PeliculaEnCartelera()==true){
               objPel.eliminar(); //Ejecutamos el método eliminar del objeto
            }
            else{
               JOptionPane.showMessageDialog(null,"NO SE PUEDE ELIMINAR LA PELICULA YA QUE SE ENCUENTRA EN CARTELERA.");
            }

            //Refrescamos la lista de productos
            CargarPeliculas();
        }
    }//GEN-LAST:event_bntEliminarActionPerformed

    private void bntEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntEditarActionPerformed
        //Obtener la fila seleccionada
        int fila = TblPeliculas.getSelectedRow();

        //Si es mayor o igual a cero, entonces, existe fila.
        if (fila >= 0) {
            //Asignamos valores de la tabla a cada control o variables
            id = Integer.parseInt(TblPeliculas.getValueAt(fila, 0).toString());
            txtNombre.setText(TblPeliculas.getValueAt(fila, 1).toString());
            cbxCategorias.setSelectedItem(TblPeliculas.getValueAt(fila, 2)); //Indicamos que "item" se debe seleccionar
            txtDuracion.setText(TblPeliculas.getValueAt(fila, 3).toString());
        }
    }//GEN-LAST:event_bntEditarActionPerformed

    private void bntGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntGuardarActionPerformed
       //Creamos el objeto Producto
        Peliculas objPel= new Peliculas();
        Object Val = cbxCategorias.getSelectedItem();
        String NmCat = (String) Val;
        //Asignamos valor a cada una de sus propiedades
        objPel.setCategoria(NmCat); //Obtener el nombre de la categoria
        objPel.setNombre(txtNombre.getText());
        objPel.setDuracion((txtDuracion.getText()));

        //Realizamos una validación: si el "ID" es "cero", entonces "registramos" el producto
        if (id == 0) {
            objPel.registrar();
        } else {
            //Caso contrario, asignamos valor al "ID" de la pelicula para poder "actualizar"
            objPel.setIdPelicula(id);
            objPel.actualizar();
        }

        //Refrescamos la lista de productos
        CargarPeliculas();

        //auto-ejecutamos el limpiar
        btnCancelar.doClick();
    }//GEN-LAST:event_bntGuardarActionPerformed

