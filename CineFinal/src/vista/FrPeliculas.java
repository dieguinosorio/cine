    public void CargarGategorias(){
        //Para "listar" Marcas, debemos crear un Objeto del mismo
        Categorias objCategoria = new Categorias();
        objCategoria.listar(); //Invocar al método listar del Objeto
        //Ese método de arriba permite llenar datos en el Colector

        //Recorremos el objeto Marca en la Colección (listaMarcas)
        for (Categorias m : Collector.listaCategorias) {
            cbxCategorias.addItem(m.getNmCategoria()); //Agregamos al comboBox
        }
    }
    
    public void CargarPeliculas(){
        Peliculas ObjPeliculas = new Peliculas();
        ObjPeliculas.listar();
        //Limpiamos todas las filas de la tabla
        ((DefaultTableModel) TblPeliculas.getModel()).setRowCount(0);
        
        //Creamos variable para las columnas
        Object obj[] = new Object[4];
         
        for (Peliculas p : Collector.listaPeliculas) {
            //Asignamos valor a cada columna
            obj[0] = p.getIdPelicula();
            obj[1] = p.getNombre();
            obj[2] = p.getCategoria();
            obj[3] = p.getDuracion();

            //Agregamos las nuevas filas
            ((DefaultTableModel) TblPeliculas.getModel()).addRow(obj);
        }
    }

